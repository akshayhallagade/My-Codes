public class Start5 {
    public static void main(String[] arg) {
        // Mathermatical Operator.(+,-,/,*,%)
        int a = 12;
        int b = 8;
        int c = a + b;
        System.out.println(c);
        // ...........................................
        // Increment Operator.(++p and --p)
        int x = 34;
        x++;
        System.out.println(x);
        // ...........................................
        // Prefix and Postfix Operator. (++p and p++)
        int p = 12;
        int q = p++;
        System.out.println(p + "  " + q); // p=13 and q=12
    }
}
