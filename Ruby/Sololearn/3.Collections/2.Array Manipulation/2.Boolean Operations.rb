########## & - operator, | - operator ############

a = [2,3,7,8]
b = [2,7,9]
print a & b   # &-operator : gives the common elements of the two arrays.

a = [2,3,7,8]
b = [2,7,9]
print a | b   # |-operator : gives the elements by joining arrays and removing the duplicates.

#NOTE: The above operators are different than (&&) and (||).