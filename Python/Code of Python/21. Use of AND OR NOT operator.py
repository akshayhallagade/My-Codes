#use of AND OR NOT operators.
print("AND operator\n")

#AND operator
a,b,c=1,2,3
print(b>a and c>a)
print(c>a and c>0,"\n\n\n")
#################

print("OR gate\n")
#OR operator
a,b,c=1,2,3
print(b>a or c>a)
print(c>0 or c>a)
print(c==4 or b!=2,"\n\n\n")
##################

print("NOT operator\n")
#NOT operator
x=5
print(not(x!=5))
print(not(x==5))
